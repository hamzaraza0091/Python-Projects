tasks = []

while True:
    print("\n1. Add Task")
    print("2. View Tasks")
    print("3. Exit")

    choice = input("Choose option: ")

    if choice == "1":
        task = input("Enter task: ")
        tasks.append(task)
        print("Task added")

    elif choice == "2":
        print("Your Tasks:")
        for t in tasks:
            print("-", t)

    elif choice == "3":
        print("Goodbye!")
        break
# hello
    else:
        print("Invalid choice")